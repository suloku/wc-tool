		void Save_ect_butClick(object sender, EventArgs e)
		{
			set_ectData();
			ectfile.fix_ect_checksum();
			if (ectfile.Edited)
				FileIO.save_data(ectfile.Data, ectfilter);
			else MessageBox.Show("Save has not been edited");
		}
